#!/usr/bin/env python3
"""Write a compact LaTeX metrics table for selected main experiment cases."""

from __future__ import annotations

import argparse
import csv
from pathlib import Path


FAMILY_LABELS = {
    "SC_baseline_tree_canopy": ("Space colonization", "Traditional"),
    "SC_ours_V32_balanced_canopy_D": ("Space colonization", "Ours"),
    "LSystem_baseline_matched_branch": ("L-system", "Traditional"),
    "LSystem_ours_V40_slim_D": ("L-system", "Ours (legacy)"),
    "LSystem_ours_V60_clean_fine_B": ("L-system", "Ours"),
    "DLA_baseline_cluster": ("DLA", "Traditional"),
    "DLA_ours_V14_table_coral_B": ("DLA", "Ours"),
    "IFS_baseline_matched_branch_tree": ("IFS", "Traditional"),
    "IFS_ours_pyrite_HQ_stage04": ("IFS", "Ours"),
}


def fmt_float(value: str, digits: int = 3) -> str:
    try:
        return f"{float(value):.{digits}f}"
    except Exception:
        return "--"


def fmt_int(value: str) -> str:
    try:
        return f"{int(float(value)):,}"
    except Exception:
        return "--"


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--surface-csv", type=Path, required=True)
    parser.add_argument("--out", type=Path, required=True)
    args = parser.parse_args()

    rows = []
    with args.surface_csv.open(newline="", encoding="utf-8") as f:
        for row in csv.DictReader(f):
            family, method = FAMILY_LABELS.get(row["label"], ("", row.get("role", "")))
            rows.append(
                {
                    "family": family,
                    "method": method,
                    "components_r0": row["components_r0"],
                    "lcr_r0": row["lcr_r0"],
                    "components_r1": row["components_r1"],
                    "lcr_r1": row["lcr_r1"],
                    "box_dim": row["box_dim_32_96"],
                    "vertices": row["vertices"],
                    "faces": row["faces"],
                    "occupied": row["occupied"],
                }
            )

    lines = [
        "% Auto-generated by assets/write_main_experiment_metrics_table_20260511.py",
        "\\begin{table}[t]",
        "\\centering",
        "\\caption{Selected main-comparison diagnostics under the same surface-voxel protocol. $r0$ reports exact occupied-surface connectivity; $r1$ allows one-voxel dilation. Visual quality is judged separately in Fig.~\\ref{fig:main-experiment-traditional-vs-ours}.}",
        "\\label{tab:main-experiment-selected-metrics}",
        "\\begin{tabular}{llrrrrr}",
        "\\toprule",
        "Family & Method & $C_{r0}$ & LCR$_{r0}$ & $C_{r1}$ & Dim. & Faces \\\\",
        "\\midrule",
    ]
    current_family = None
    for row in rows:
        if current_family is not None and row["family"] != current_family:
            lines.append("\\addlinespace")
        current_family = row["family"]
        lines.append(
            f"{row['family']} & {row['method']} & {fmt_int(row['components_r0'])} & "
            f"{fmt_float(row['lcr_r0'])} & {fmt_int(row['components_r1'])} & "
            f"{fmt_float(row['box_dim'])} & {fmt_int(row['faces'])} \\\\"
        )
    lines += [
        "\\bottomrule",
        "\\end{tabular}",
        "\\end{table}",
        "",
    ]
    args.out.parent.mkdir(parents=True, exist_ok=True)
    args.out.write_text("\n".join(lines), encoding="utf-8")
    print(args.out)


if __name__ == "__main__":
    main()
