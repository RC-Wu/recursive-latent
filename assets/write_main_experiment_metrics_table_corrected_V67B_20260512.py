#!/usr/bin/env python3
"""Write the corrected V67B main-comparison metrics table."""

from __future__ import annotations

import argparse
import csv
from pathlib import Path


LABELS = {
    "SC_baseline_tree_canopy": ("Space colonization", "Traditional"),
    "SC_ours_V32_balanced_canopy_D": ("Space colonization", "Ours"),
    "LSystem_baseline_OBJ_neutral_upright": ("L-system", "Traditional"),
    "LSystem_ours_V67_tapered_dense_B": ("L-system", "Ours"),
    "DLA_baseline_cluster": ("DLA/frontier", "Traditional"),
    "DLA_ours_V8_lace_reef_coral_A": ("DLA/frontier", "Ours"),
    "IFS_baseline_matched_branch_tree": ("IFS/transform", "Traditional"),
    "IFS_ours_V21_branch_tree_natural_bark": ("IFS/transform", "Ours"),
}


ORDER = list(LABELS)


def fmt_float(value: str, digits: int = 3) -> str:
    try:
        return f"{float(value):.{digits}f}"
    except Exception:
        return "--"


def fmt_int(value: str) -> str:
    try:
        return f"{int(float(value)):,}"
    except Exception:
        return "--"


def read_csv(path: Path) -> dict[str, dict[str, str]]:
    with path.open(newline="", encoding="utf-8") as f:
        return {row["label"]: row for row in csv.DictReader(f)}


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--surface-csv", type=Path, required=True)
    parser.add_argument("--mesh-csv", type=Path, required=True)
    parser.add_argument("--out", type=Path, required=True)
    args = parser.parse_args()

    surface = read_csv(args.surface_csv)
    mesh = read_csv(args.mesh_csv)

    lines = [
        "% Auto-generated by assets/write_main_experiment_metrics_table_corrected_V67B_20260512.py",
        "\\begin{table*}[t]",
        "\\centering",
        "\\small",
        "\\setlength{\\tabcolsep}{3pt}",
        "\\caption{Main traditional-target comparison diagnostics for the corrected four-family subset. $C_{r0}$ and LCR$_{r0}$ are exact surface-sampled voxel connectivity; $C_{r1}$ allows one-voxel dilation for aliasing and tiny texture-export islands. Welded comp. is a tolerance-based mesh diagnostic at tolerance $0.002$. Visual quality is evaluated in Figure~\\ref{fig:main-experiment-traditional-vs-ours}.}",
        "\\label{tab:main-experiment-selected-metrics}",
        "\\begin{tabular}{llrrrrrr}",
        "\\toprule",
        "Family & Method & $C_{r0}$ & LCR$_{r0}$ & $C_{r1}$ & Faces & Welded comp. & Box dim. \\\\",
        "\\midrule",
    ]

    current_family = None
    for label in ORDER:
        srow = surface[label]
        mrow = mesh[label]
        family, method = LABELS[label]
        if current_family is not None and family != current_family:
            lines.append("\\addlinespace")
        current_family = family
        lines.append(
            f"{family} & {method} & {fmt_int(srow['components_r0'])} & "
            f"{fmt_float(srow['lcr_r0'])} & {fmt_int(srow['components_r1'])} & "
            f"{fmt_int(srow['faces'])} & {fmt_int(mrow['welded_component_count'])} & "
            f"{fmt_float(srow['box_dim_32_96'])} \\\\"
        )

    lines += [
        "\\bottomrule",
        "\\end{tabular}",
        "\\end{table*}",
        "",
    ]
    args.out.parent.mkdir(parents=True, exist_ok=True)
    args.out.write_text("\n".join(lines), encoding="utf-8")
    print(args.out)


if __name__ == "__main__":
    main()
